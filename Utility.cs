using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Week4_Project
{
    public static class Utility
    {

        public static int GetArea(int L, int W)
        {
            return (L * W);
        }
        public static int Perimeter(int L, int W)
        {
            return (2 * (L * W));
        }

    }
}
